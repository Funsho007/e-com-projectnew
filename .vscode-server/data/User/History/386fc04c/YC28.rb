This application uses data from the Star Wars API (SWAPI) and Faker gem to populate its database with information about the Star Wars universe. Below are the endpoints used and their corresponding models:

1. **Planets**
   - **Endpoint:** [https://swapi.dev/api/planets/](https://swapi.dev/api/planets/)
   - **Model:** `Planet`

2. **People**
   - **Endpoint:** [https://swapi.dev/api/people/](https://swapi.dev/api/people/)
   - **Model:** `Person`

3. **Starships**
   - **Endpoint:** [https://swapi.dev/api/starships/](https://swapi.dev/api/starships/)
   - **Model:** `Starship`

4. **Vehicles**
   - **Endpoint:** [https://swapi.dev/api/vehicles/](https://swapi.dev/api/vehicles/)
   - **Model:** `Vehicle`

5. **Films**
   - **Endpoint:** [https://swapi.dev/api/films/](https://swapi.dev/api/films/)
   - **Model:** `Film`

6. **Species**
   - **Endpoint:** [https://swapi.dev/api/species/](https://swapi.dev/api/species/)
   - **Model:** `Species`
